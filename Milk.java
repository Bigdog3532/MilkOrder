public class Milk {
    private int FCM;
    private int SM;
    private int Tond;
    
    public Milk(int fCM, int sM, int tond) {
        FCM = fCM;
        SM = sM;
        Tond = tond;
    }

    public int getFCM() {
        return FCM;
    }

    public int getSM() {
        return SM;
    }

    public int getTond() {
        return Tond;
    }
}
